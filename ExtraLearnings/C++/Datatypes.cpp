#include<bits/stdc++.h>
using namespace std;

int main(){
    // Note: The size of the datatypes may vary from compiler to compiler

    // Signed int -> 4 bytes, [-2^31 to 2^31 - 1]
    int a = 10;

    // Unsigned int -> 4 bytes, [0 to 2^32 - 1]
    unsigned int b = 10;

    // Signed long -> 4 bytes (or more), [-2^31 to 2^31 - 1] (or more)
    long c = 10;

    // Unsigned long -> 4 bytes (or more), [0 to 2^32 - 1] (or more)
    unsigned long d = 10;

    // Signed long long -> 8 bytes (or more), [-2^63 to 2^63 - 1] (or more)
    long long e = 10;

    // Unsigned long long -> 8 bytes (or more), [0 to 2^64 - 1] (or more)
    unsigned long long f = 10;

    // Float -> 4 bytes, 6 decimal places, [-3.4 * 10^38 to 3.4 * 10^38]
    float g = 10.123456;

    // Double -> 8 bytes, 15 decimal places, [-1.7 * 10^308 to 1.7 * 10^308]
    double h = 10.123456789012345;

    // Long double -> 10 bytes (or more), 19 decimal places (or more), [-1.1 * 10^4932 to 1.1 * 10^4932]
    long double i = 10.12345678901234567890123456789012345678901234567890123456789012345678901234567890;

    // Char -> 1 byte, [-128 to 127]
    char j = 'a';

    // Bool -> 1 byte, [0 to 1]
    bool k = true;

    // String -> 1 byte per character, [0 to 2^32 - 1]
    string l = "Hello World";



    return 0;
}